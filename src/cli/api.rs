/// Production `QuoteApi` backed by the global longbridge `QuoteContext`.
pub struct LbQuoteApi {
    ctx: &'static longbridge::quote::QuoteContext,
}

impl LbQuoteApi {
    pub fn new(ctx: &'static longbridge::quote::QuoteContext) -> Self {
        Self { ctx }
    }
}

#[async_trait]
impl QuoteApi for LbQuoteApi {
    async fn quote(&self, symbols: Vec<String>) -> Result<Vec<SecurityQuote>> {
        Ok(self.ctx.quote(symbols).await?)
    }

    async fn depth(&self, symbol: String) -> Result<SecurityDepth> {
        Ok(self.ctx.depth(symbol).await?)
    }

    async fn brokers(&self, symbol: String) -> Result<SecurityBrokers> {
        Ok(self.ctx.brokers(symbol).await?)
    }

    async fn trades(&self, symbol: String, count: usize) -> Result<Vec<Trade>> {
        Ok(self.ctx.trades(symbol, count).await?)
    }

    async fn intraday(&self, symbol: String) -> Result<Vec<IntradayLine>> {
        use longbridge::quote::TradeSessions;
        Ok(self.ctx.intraday(symbol, TradeSessions::Intraday).await?)
    }

    async fn candlesticks(
        &self,
        symbol: String,
        period: Period,
        count: usize,
        adjust: AdjustType,
    ) -> Result<Vec<Candlestick>> {
        use longbridge::quote::TradeSessions;
        Ok(self
            .ctx
            .candlesticks(symbol, period, count, adjust, TradeSessions::Intraday)
            .await?)
    }

    async fn history_candlesticks_by_date(
        &self,
        symbol: String,
        period: Period,
        adjust: AdjustType,
        start: Option<Date>,
        end: Option<Date>,
    ) -> Result<Vec<Candlestick>> {
        use longbridge::quote::TradeSessions;
        Ok(self
            .ctx
            .history_candlesticks_by_date(
                symbol,
                period,
                adjust,
                start,
                end,
                TradeSessions::Intraday,
            )
            .await?)
    }

    async fn history_candlesticks_by_offset(
        &self,
        symbol: String,
        period: Period,
        adjust: AdjustType,
        count: usize,
    ) -> Result<Vec<Candlestick>> {
        use longbridge::quote::TradeSessions;
        Ok(self
            .ctx
            .history_candlesticks_by_offset(
                symbol,
                period,
                adjust,
                false,
                None,
                count,
                TradeSessions::Intraday,
            )
            .await?)
    }

    async fn static_info(&self, symbols: Vec<String>) -> Result<Vec<SecurityStaticInfo>> {
        Ok(self.ctx.static_info(symbols).await?)
    }

    async fn calc_indexes(
        &self,
        symbols: Vec<String>,
        indexes: Vec<CalcIndex>,
    ) -> Result<Vec<SecurityCalcIndex>> {
        Ok(self.ctx.calc_indexes(symbols, indexes).await?)
    }

    async fn capital_flow(&self, symbol: String) -> Result<Vec<CapitalFlowLine>> {
        Ok(self.ctx.capital_flow(symbol).await?)
    }

    async fn capital_distribution(&self, symbol: String) -> Result<CapitalDistributionResponse> {
        Ok(self.ctx.capital_distribution(symbol).await?)
    }

    async fn market_temperature(&self, market: Market) -> Result<MarketTemperature> {
        Ok(self.ctx.market_temperature(market).await?)
    }

    async fn history_market_temperature(
        &self,
        market: Market,
        start: Date,
        end: Date,
    ) -> Result<HistoryMarketTemperatureResponse> {
        Ok(self
            .ctx
            .history_market_temperature(market, start, end)
            .await?)
    }

    async fn trading_session(&self) -> Result<Vec<MarketTradingSession>> {
        Ok(self.ctx.trading_session().await?)
    }

    async fn trading_days(
        &self,
        market: Market,
        begin: Date,
        end: Date,
    ) -> Result<MarketTradingDays> {
        Ok(self.ctx.trading_days(market, begin, end).await?)
    }

    async fn security_list(&self, market: Market) -> Result<Vec<Security>> {
        Ok(self.ctx.security_list(market, None).await?)
    }

    async fn participants(&self) -> Result<Vec<ParticipantInfo>> {
        Ok(self.ctx.participants().await?)
    }

    async fn subscriptions(&self) -> Result<Vec<Subscription>> {
        Ok(self.ctx.subscriptions().await?)
    }

    async fn option_quote(&self, symbols: Vec<String>) -> Result<Vec<OptionQuote>> {
        Ok(self.ctx.option_quote(symbols).await?)
    }

    async fn option_chain_expiry_date_list(&self, symbol: String) -> Result<Vec<Date>> {
        Ok(self.ctx.option_chain_expiry_date_list(symbol).await?)
    }

    async fn option_chain_info_by_date(
        &self,
        symbol: String,
        expiry_date: Date,
    ) -> Result<Vec<StrikePriceInfo>> {
        Ok(self
            .ctx
            .option_chain_info_by_date(symbol, expiry_date)
            .await?)
    }

    async fn warrant_quote(&self, symbols: Vec<String>) -> Result<Vec<WarrantQuote>> {
        Ok(self.ctx.warrant_quote(symbols).await?)
    }

    async fn warrant_list(&self, symbol: String) -> Result<Vec<WarrantInfo>> {
        use longbridge::quote::{SortOrderType, WarrantSortBy};
        Ok(self
            .ctx
            .warrant_list(
                symbol,
                WarrantSortBy::LastDone,
                SortOrderType::Descending,
                None,
                None,
                None,
                None,
                None,
            )
            .await?)
    }

    async fn warrant_issuers(&self) -> Result<Vec<IssuerInfo>> {
        Ok(self.ctx.warrant_issuers().await?)
    }

    async fn watchlist(&self) -> Result<Vec<WatchlistGroup>> {
        Ok(self.ctx.watchlist().await?)
    }

    async fn create_watchlist_group(&self, name: String) -> Result<i64> {
        use longbridge::quote::RequestCreateWatchlistGroup;
        Ok(self
            .ctx
            .create_watchlist_group(RequestCreateWatchlistGroup::new(name))
            .await?)
    }

    async fn delete_watchlist_group(&self, id: i64) -> Result<()> {
        Ok(self.ctx.delete_watchlist_group(id, false).await?)
    }

    async fn update_watchlist_group(&self, req: RequestUpdateWatchlistGroup) -> Result<()> {
        Ok(self.ctx.update_watchlist_group(req).await?)
    }
}

